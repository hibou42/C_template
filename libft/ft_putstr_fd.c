/******************************************************************************
	Write the string s on the output fd
		Return :
			nothing, just hardwork
******************************************************************************/

#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], sizeof(s[i]));
		i++;
	}
}
