/******************************************************************************
	Write the string s and a '\n' at the end on the output fd
		Return :
			nothing, just hardwork
******************************************************************************/

#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], sizeof(s[i]));
		i++;
	}
	write(fd, "\n", 1);
}
