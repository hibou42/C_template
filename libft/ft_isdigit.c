/******************************************************************************
	Determines if an integer is a digit charcter
		Return : 
			the input if right
			0 if false
******************************************************************************/

#include "libft.h"

int	ft_isdigit(int nb)
{
	if (nb >= '0' && nb <= '9')
		return (nb);
	else
		return (0);
}
