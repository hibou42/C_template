/******************************************************************************
	Write the int n on the output fd
		Return :
			nothing, just hardwork
******************************************************************************/

#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		nb;
	char	res;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		nb = n;
		if (nb >= 10)
		{
			ft_putnbr_fd(nb / 10, fd);
			ft_putnbr_fd(nb % 10, fd);
		}
		if (nb < 10)
		{
			res = nb + '0';
			write(fd, &res, 1);
		}
	}
}
