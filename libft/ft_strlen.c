/******************************************************************************
	Count the number of character in the string in parameter
		Return : 
			int of the count
******************************************************************************/

#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
