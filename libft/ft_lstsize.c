/******************************************************************************
	Count who many struct do you have in the list
		Return :
			the number of struct
******************************************************************************/

#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		i++;
		lst = lst->next;
	}
	return (i);
}
