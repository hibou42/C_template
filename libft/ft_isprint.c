/******************************************************************************
	Determines if an integer is a printable charcter
		Return : 
			the input if right
			0 if false
******************************************************************************/

#include "libft.h"

int	ft_isprint(int nb)
{
	if (nb > 31 && nb < 127)
		return (nb);
	else
		return (0);
}
